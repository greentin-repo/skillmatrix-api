package com.greentin.enovation.beans;

public class MoodIndicatorDto {

	private int deptId;

	private String deptName;

	private int rating;

	private String empCount;

	private String month;

	private String year;

	private String emojiPercentage;

	private String totalEmpCount;

	public int getDeptId() {
		return deptId;
	}

	public void setDeptId(int deptId) {
		this.deptId = deptId;
	}

	public String getDeptName() {
		return deptName;
	}

	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}

	public int getRating() {
		return rating;
	}

	public void setRating(int rating) {
		this.rating = rating;
	}

	public String getEmpCount() {
		return empCount;
	}

	public void setEmpCount(String empCount) {
		this.empCount = empCount;
	}

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getEmojiPercentage() {
		return emojiPercentage;
	}

	public void setEmojiPercentage(String emojiPercentage) {
		this.emojiPercentage = emojiPercentage;
	}

	public String getTotalEmpCount() {
		return totalEmpCount;
	}

	public void setTotalEmpCount(String totalEmpCount) {
		this.totalEmpCount = totalEmpCount;
	}



}
